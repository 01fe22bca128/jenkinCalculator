/**
 * The Arithmatic_Op class contains methods for performing arithmetic operations such as squaring,
 * cubing, and finding the modulus of two numbers.
 */
public class Extended_Calculator{
    int square(int operand_1){
        return(operand_1*operand_1);
    }
    float cube(float operand_1){
        return(operand_1*operand_1*operand_1);
    }
    float modulus(float operand_1,float operand_2){
        return(operand_1%operand_2);
    }
   
}
